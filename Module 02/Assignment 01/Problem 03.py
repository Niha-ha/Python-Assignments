''' Python program that creates a new list containing only the
 duplicate elements from the given list: [1, 5, 6, 5, 1, 2, 3]'''


list1 = [1, 5, 6, 5, 1, 2, 3]
duplicate = []
for item in list1:
    if list1.count(item)>1 and item not in duplicate:   # .count() checks how many times an item appears
            duplicate.append(item)                      # add item to duplicates list

print("New list with duplicate item:", duplicate)
